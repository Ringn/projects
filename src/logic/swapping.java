package logic;

public class swapping {
	public static void main(String[] args) 
	{
		int a=20;
		int b=30;
		int c;
		System.out.println("---orignal---");
		System.out.println("a= "+a+"\nb= "+b);
		c=a;//c=20
		a=b;//a=30;
		b=c;//b=20'
		System.out.println("after swapping");
		System.out.println("a="+a+"\nb="+b);
	}

}
